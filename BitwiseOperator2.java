package JavaPractice;

public class BitwiseOperator2 {

	public static void main(String[] args) {
		int a=32;
		int b=12;
		int c;
		
		c= a>>2;
		System.out.println("a >> 2= "+c);
		
		c= a>>3;
		System.out.println("a >> 3= "+c);
		
		c= b<<3;
		System.out.println("b << 3= "+c);
		
	}

}
