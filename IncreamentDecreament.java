package JavaPractice;

public class IncreamentDecreament {

	public static void main(String[] args) {
		
		int x=25;
		int y;
		y= x++;
		y= x;
		System.out.println(y);
		y= ++x;
		System.out.println(y);
		y= --x;
		System.out.println(y);
		y= x--;
		y= x;
		System.out.println(y);
		
	}

}
